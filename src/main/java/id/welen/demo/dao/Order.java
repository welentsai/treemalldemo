package id.welen.demo.dao;

public class Order {

    private String name;
    private String logo;
    private Status status;
    private String date;

    public Order(String name, String logo, Status status, String date) {
        this.name = name;
        this.logo = logo;
        this.status = status;
        this.date = date;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLogo() {
        return logo;
    }

    public void setLogo(String logo) {
        this.logo = logo;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public Status getStatus() {
        return status;
    }

    public void setStatus(Status status) {
        this.status = status;
    }

}
